import { getWarnCount, addWarn, resetWarn, getGroupSettings } from '../../database/config.js';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';
import { resolveTargetJid, resolvePhoneNumber } from '../../lib/lidResolver.js';

const DEV_NUMBER = '255752593977';

export default {
    name: 'warn',
    alias: ['warns', 'warnlist'],
    description: 'Warn a group member',
    run: async (context) => {
        const { client, m, isAdmin, isBotAdmin } = context;
        const fq = getFakeQuoted(m);
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });

        if (!m.isGroup) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Group only command.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }
        if (!isAdmin) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Admin only.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }

        let rawJid = m.quoted?.sender || m.mentionedJid?.[0];
        if (!rawJid) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Reply to or mention the rat you wanna warn.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }

        const groupMetadata = await client.groupMetadata(m.chat);
        const participants = groupMetadata.participants;
        const target = resolveTargetJid(rawJid, participants);
        if (!target) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Couldn't find that person in this group.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }

        const _targetNum = target.split('@')[0].replace(/\D/g, '');
        const _botNum = (client.user.id.split(':')[0].split('@')[0].replace(/\D/g, ''));
        if (_targetNum === DEV_NUMBER || _targetNum === _botNum) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң That command cannot be used on the dev or the bot.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }

        try {
            const gs = await getGroupSettings(m.chat);
            const warnLimit = gs.warn_limit || 3;
            const userNum = target.split('@')[0].split(':')[0];
            const count = await addWarn(m.chat, userNum);

            if (count >= warnLimit) {
                await resetWarn(m.chat, userNum);
                try { await client.groupParticipantsUpdate(m.chat, [target], 'remove'); } catch {}
                await client.sendMessage(m.chat, { react: { text: 'вң…', key: m.reactKey } });
                return client.sendMessage(m.chat, {
                    text: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« KICKED вүӘв”Ғв”Ғв”Ғ\nв”ң @${userNum} hit \`${count}/${warnLimit}\` warns.\nв”ң Bye bye rat рҹ‘Ӣ\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`,
                    mentions: [target]
                }, { quoted: fq });
            }

            await client.sendMessage(m.chat, { react: { text: 'вң…', key: m.reactKey } });
            return client.sendMessage(m.chat, {
                text: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« WARNED вүӘв”Ғв”Ғв”Ғ\nв”ң @${userNum}\nв”ң Warns: \`${count}/${warnLimit}\`\nв”ң One more and it's the door.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`,
                mentions: [target]
            }, { quoted: fq });
        } catch (error) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Failed to warn: ${error.message?.slice(0, 60)}\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }
    }
};
