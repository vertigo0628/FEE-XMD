import middleware from '../../utils/botUtil/middleware.js';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';
import { resolveTargetJid } from '../../lib/lidResolver.js';

const DEV_NUMBER = '255752593977';

export default {
  name: 'remove',
  aliases: ['kick', 'yeet', 'boot', 'removemember'],
  description: 'Removes a user from a group',
  run: async (context) => {
    await middleware(context, async () => {
      const { client, m, prefix } = context;
      const fq = getFakeQuoted(m);
      await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });

      let rawJid = null;
      if (m.mentionedJid && m.mentionedJid.length > 0) rawJid = m.mentionedJid[0];
      if (!rawJid && m.quoted?.sender) rawJid = m.quoted.sender;

      if (!rawJid) {
        await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
        return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Mention or quote a user. ${prefix}kick @user\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
      }

      const groupMetadata = await client.groupMetadata(m.chat);
      const participants = groupMetadata.participants;
      const targetJid = resolveTargetJid(rawJid, participants);
      const botJid = (client.user.id.split(':')[0].split('@')[0].replace(/\D/g, '')) + '@s.whatsapp.net';

      if (!targetJid) {
        await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
        return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Couldn't find that person in this group.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
      }

      const _targetNum = targetJid.split('@')[0].replace(/\D/g, '');
      const _botNum = botJid.split('@')[0].replace(/\D/g, '');
      if (_targetNum === DEV_NUMBER || _targetNum === _botNum) {
        await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
        return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң That command cannot be used on the dev or the bot.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
      }

      try {
        await client.groupParticipantsUpdate(m.chat, [targetJid], 'remove');
        await client.sendMessage(m.chat, { react: { text: 'вң…', key: m.reactKey } });
        await client.sendMessage(m.chat, {
          text: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« KICKED вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң @${targetJid.split('@')[0]} got yeeted out.\nв”ң Good riddance, trash.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`,
          mentions: [targetJid]
        }, { quoted: fq });
      } catch (error) {
        await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
        await m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң Couldn't kick that user.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
      }
    });
  }
};
