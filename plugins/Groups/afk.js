import afkFeature from '../../features/afk.js';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';

export default {
    name: 'afk',
    alias: ['away', 'brb'],
    description: 'Set yourself as AFK',
    run: async (context) => {
        const { client, m } = context;
        const fq = getFakeQuoted(m);
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
        const senderNum = m.sender.split('@')[0].split(':')[0];
        const reason = context.text || context.q || 'no reason';

        if (afkFeature.isAfk(senderNum)) {
            afkFeature.removeAfk(senderNum);
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ң AFK removed. Welcome back, ghost. рҹ‘ҒпёҸ\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }

        afkFeature.setAfk(senderNum, reason);
        return client.sendMessage(m.chat, {
            text: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« AFK SET вүӘв”Ғв”Ғв”Ғ\nв”ң @${senderNum} went AFK.\nв”ң Reason: ${reason}\nв”ң Don't bother them. рҹҡ«\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`,
            mentions: [m.sender]
        }, { quoted: fq });
    }
};
